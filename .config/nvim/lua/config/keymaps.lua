vim.keymap.set("n", "<C-h>", "<C-w>h") -- 左のpaneへ
vim.keymap.set("n", "<C-l>", "<C-w>l") -- 右のpaneへ
vim.keymap.set("n", "<C-j>", "<C-w>j") -- 下のpaneへ
vim.keymap.set("n", "<C-k>", "<C-w>k") -- 上のpaneへ

